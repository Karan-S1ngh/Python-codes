# Explain all comparison operators of python with suitable examples of each


a = int(input("Enter a : "))
b = int(input("Enter b : "))

# In Python, we have the following comparison operators:

# 1. Equal to (==): It is used to check if two values are equal.
# Example:
c = a == b
print("a Equal to b :",c)

# 2. Not equal to (!=): It is used to check if two values are not equal.
# Example:
c = a != b
print("a Not equal to b :",c)

# 3. Greater than (>): It is used to check if the first value is greater than the second value.
# Example:
c = a > b
print("a Greater than b :",c)

# 4. Less than (<): It is used to check if the first value is less than the second value.
# Example:
c = a < b
print("a Less than b :",c)

# 5. Greater than or equal to (>=): It is used to check if the first value is greater than or equal to the second value.
# Example:
c = a >= b
print("a Greater than or equal to b :",c)

# 6. Less than or equal to (<=): It is used to check if the first value is less than or equal to the second value.
# Example:
c = a <= b
print("a Less than or equal to b :",c)




