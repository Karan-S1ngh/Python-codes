# Explain any six arithmetic operators of python with suitable examples of each


a = int(input("Enter first number : "))
b = int(input("Enter second number : "))

# In Python, we have the following arithmetic operators:

# 1. Addition (+): It is used to add two numbers.
# Example:

c = a + b
print("Addition :",c)

# 2. Subtraction (-): It is used to subtract two numbers.
# Example:
c = a - b
print("Subtraction :",c)

# 3. Multiplication (*): It is used to multiply two numbers.
# Example:
c = a * b
print("Multiplication :",c)

# 4. Division (/): It is used to divide two numbers.
# Example:
c = b / a
print("Division :",c)

# 5. Modulus (%): It is used to find the remainder when one number is divided by another number.
# Example:
c = b % a
print("Modulus :",c)

# 6. Exponentiation (**): It is used to raise one number to the power of another number.
# Example:
c = a ** b
print("Exponentiation :",c)

# 7. Floor Division (//): It is used to divide two numbers and return the integer part of the result.
# Example:
c = b // a
print("Floor Division :",c)




