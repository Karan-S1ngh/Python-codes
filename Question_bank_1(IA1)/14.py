# Explain following operators of python with suitable examples of each. 
# a. is, 
# b. in, 
# c. not in


a = 10
b = 20
c = 10
d = [10,20,30,40,50]

# is
# It is used to check if two variables are referring to the same object.
# Example:
print("1 : a is c =",a is c)

# in
# It is used to check if a value is present in a sequence (list, tuple, string etc.).
# Example:
print("2 : a in d =",a in d)

# not in
# It is used to check if a value is not present in a sequence (list, tuple, string etc.).
# Example:
print("3 : b not in d =",b not in d)




'''OUTPUT
1 : a is c = True
2 : a in d = True
3 : b not in d = False
'''
