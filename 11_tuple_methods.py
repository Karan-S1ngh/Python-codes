# Tuple methods

t = (1,2,89,78)
print("1 :",t)

print("2 :",t.count(1))
# prints how many times 1 is present in tuple

print("3 :",t.index(89))
# prints index of 89 in tuple
# if 89 is not present in tuple then error will occur

print("4 :",len(t))
# prints length of tuple

# operations that perform changes are invalid for tuple



'''OUTPUT
1 : (1, 2, 89, 78)
2 : 1
3 : 2
4 : 4
'''
