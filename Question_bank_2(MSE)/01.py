# What are classes and objects? how are they created ?


# Classes are the blueprint of objects. They are the template for objects.
# Objects are the instances of classes. They are the real world entities.
# Objects are created using the class name followed by the object name and the assignment operator.
# The object name is followed by the class name and the object name followed by the parenthesis.
# The parenthesis is used to call the constructor of the class.

# Example:
class ClassName:
    pass
object_name = ClassName()
object_name.method_name()
